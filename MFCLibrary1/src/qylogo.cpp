void
draw_line(double point[][2],short num,double fscale,ads_point basepoint)
{//画多义线.
   AcDbPolyline *ppoly = new AcDbPolyline;
   AcDbVertex cvertex;
   double strwidth = 0;
   double endwith = 0;
   double bulge   = 0;
   AcGePoint2d point2d;
   double x,y;
   for(int i = 0; i < num;i++)
   {
      x = point[i][0]*fscale +basepoint[0];
	  y = point[i][1]*fscale +basepoint[1];
	  point2d.set(x,y);
	  ppoly->addVertexAt(i,point2d,bulge,strwidth,endwith);
   }

   ppoly->setClosed(Adesk::kTrue);
   addLayer(_T("mpbk"));
   ppoly->setLayer(_T("mpbk"));
   join2database(ppoly);
   ppoly->close();
}


