void
draw_date(double fscale,ads_point basepoint)
{//负责生成日期的图形.
			  ads_point base_pt;
			  ads_point_set(basepoint,base_pt);//将原始点转移过来,避免对形参改动.

			  AcDbDate cdate(AcDbDate::kInitLocalTime);//取此时此刻的日期:月-日-年;
			  short ryear,rmonth,rday;
			  cdate.getDate(rmonth,rday,ryear);
			  short sone,stwo,sthree,sfour,sfive,ssix,sseven,seight;//十个数字;
			  
			  sone = ryear/1000;  //计算年份第一个数字.
			  stwo = (ryear-sone*1000)/100;
			  sthree = (ryear - sone*1000 - stwo*100)/10;
			  sfour = ryear - sone*1000 - stwo*100 - sthree*10;
			  sfive = rmonth/10;
			  ssix = rmonth - sfive*10;
			  sseven = rday/10;
			  seight = rday - sseven*10;
			  const short sten = 10;//注意:用来标识"-"号.
			  
			  //base_pt[0]+= 0.4*fscale;//画第1个日期数字;			  
			  draw_number(sone,fscale,base_pt);

			  base_pt[0]+= 0.4*fscale;//画第2个日期数字;
			  draw_number(stwo,fscale,base_pt);

			  base_pt[0]+= 0.4*fscale;//画第3个日期数字;			  
			  draw_number(sthree,fscale,base_pt);

			  base_pt[0]+= 0.4*fscale;//画第4个日期数字;			  
			  draw_number(sfour,fscale,base_pt);

			  base_pt[0]+= 0.4*fscale;//画"-"号;
			  draw_number(sten,fscale,base_pt);

			  base_pt[0]+= 0.4*fscale;//画第5个日期数字;			  
			  draw_number(sfive,fscale,base_pt);

			  base_pt[0]+= 0.4*fscale;//画第6个日期数字;
			  draw_number(ssix,fscale,base_pt);

			  base_pt[0]+= 0.4*fscale;//画"-"号;	
			  draw_number(sten,fscale,base_pt);	

			  base_pt[0]+= 0.4*fscale;//画第7个日期数字;
			  draw_number(sseven,fscale,base_pt);

			  base_pt[0]+= 0.4*fscale;//画第8个日期数字;
			  draw_number(seight,fscale,base_pt);			  
		  }



