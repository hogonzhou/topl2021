{
	AcGeMatrix3d mtx;
	AcGeVector3d vtr;

	AcDbPolyline* ppl = NULL;
	AcDbEntity* pent = NULL;

	for(int i = 0; i < 10; i++)
	{
		vtr.x = m_insPtOfNum[i][0];
		vtr.y = m_insPtOfNum[i][1];
		mtx.setTranslation(vtr);
		m_numberAndDot[m_date[i]]->getTransformedCopy(mtx,pent);
		ppl = (AcDbPolyline*)pent;
		pent->close();
		m_allPlines.push_back(ppl);
	}
}



void 
CLtlogo::drawLogoWithParam()
{
	try
	{
		getUserInput();
	}
	catch(...)
	{
		acutPrintf(_T("\n输入有误，退出...\n"));
		return;
	}

	drawSM();
	drawAllNumAndDot();	
	getDate();
	drawDate();

	AcGeMatrix3d mtx;
	AcGeVector3d vtr;	
	mtx.setToScaling(m_scale);	
	vtr.x = m_basePt[0];
	vtr.y = m_basePt[1];
	mtx.setTranslation(vtr);

	addLayer(_T("f_logo"));
	vector<AcDbPolyline*>::iterator itr = m_allPlines.begin();
	for(; itr != m_allPlines.end(); itr++)
	{
		((AcDbPolyline*)(*itr))->transformBy(mtx);		
		(*itr)->setLayer(_T("f_logo"));
		join2database(*itr);
		(*itr)->close();
	}	
}

