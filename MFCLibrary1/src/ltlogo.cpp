/*
*    get the input of the user
*/
void 
CLtlogo::getUserInput() throw(int)
{
	int flag;
	do
	{
		flag = acedGetPoint(NULL,_T("\nPls input the insert point:"),m_basePt);
		if(flag == RTCAN)
		{
			acutPrintf(_T("\nUser cancel it,exit..."));
			throw USERCANCEL;
		}
	}while(flag != RTNORM);	


	do
	{
		flag = acedGetReal(_T("\nPls input the scale of the logo:"),&m_scale);
		if(flag == RTCAN)
		{
			acutPrintf(_T("\nUser cancel it,exit..."));
			throw USERCANCEL;
		}
	}
	while(flag != RTNORM);
}



/*
*      ;
*/
void
CLtlogo::getDate()
{
	short iYear,iDay,iMonth;

	AcDbDate objDate(AcDbDate::kInitLocalTime);
	iYear = objDate.year();	
	iMonth = objDate.month();
	iDay = objDate.day();
	
	m_date[0] = iYear / 1000;
	m_date[1] = (iYear - m_date[0]*1000) / 100;
	m_date[2] = (iYear - m_date[0]*1000 - m_date[1]*100) / 10;
	m_date[3] = iYear - m_date[0]*1000 - m_date[1]*100 - m_date[2]*10;

	m_date[4] = 10; //表示.号

	m_date[5] = iMonth / 10;
	m_date[6] = iMonth - m_date[5]*10;

	m_date[7] = 10;// 表示.号

	m_date[8] = iDay / 10;
	m_date[9] = iDay - m_date[8]*10;
}


